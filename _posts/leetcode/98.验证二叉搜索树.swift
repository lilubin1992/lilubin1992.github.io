// @lc code=start
/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     public var val: Int
 *     public var left: TreeNode?
 *     public var right: TreeNode?
 *     public init() { self.val = 0; self.left = nil; self.right = nil; }
 *     public init(_ val: Int) { self.val = val; self.left = nil; self.right = nil; }
 *     public init(_ val: Int, _ left: TreeNode?, _ right: TreeNode?) {
 *         self.val = val
 *         self.left = left
 *         self.right = right
 *     }
 * }
 */
class Solution {
    func helper(_ lowwer: Int, _ upper: Int, _ root: TreeNode?) -> Bool {
        guard let lroot = root else {
            return true
        }

        guard lroot.val > lowwer && lroot.val < upper else {
            return false
        }

        return helper(lowwer, lroot.val, lroot.left) && helper(lroot.val, upper, lroot.right)
    }

    func isValidBST(_ root: TreeNode?) -> Bool {
        return helper(Int.min, Int.max, root)
    }
}
